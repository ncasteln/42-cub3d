#include "cub3d.h"

int	is_blank_line(char *s)
{
	int	i;

	i = 0;
	while (s[i] && (s[i] == ' ' || s[i] == '\t'))
		i++;
	if (s[i] == '\n')
		return (1);
	return (0);
}
