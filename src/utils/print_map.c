#include "cub3d.h"

void	print_map(char **p)
{
	int	i;

	i = 0;
	if (p)
	{
		ft_printf("--- MAP ---\n");
		while (p[i])
		{
			ft_printf("%s", p[i]);
			i++;
		}
	}
}
